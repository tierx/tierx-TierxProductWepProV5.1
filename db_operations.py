def sync_channel_numbers(actual_channel_name):
    """‡∏ã‡∏¥‡∏á‡∏Ñ‡πå‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡∏à‡∏≤‡∏Å‡∏ä‡∏∑‡πà‡∏≠‡∏ä‡πà‡∏≠‡∏á‡∏à‡∏£‡∏¥‡∏á‡∏Å‡∏±‡∏ö‡∏ó‡∏µ‡πà‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡πÑ‡∏ß‡πâ
    
    Args:
        actual_channel_name (str): ‡∏ä‡∏∑‡πà‡∏≠‡∏ä‡πà‡∏≠‡∏á‡∏à‡∏£‡∏¥‡∏á‡∏à‡∏≤‡∏Å Discord
        
    Returns:
        bool: True ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡∏ã‡∏¥‡∏á‡∏Ñ‡πå
    """
    import re
    
    # ‡∏´‡∏≤‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡πÉ‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡∏ä‡πà‡∏≠‡∏á‡∏à‡∏£‡∏¥‡∏á
    number_match = re.search(r'(\d+)$', actual_channel_name)
    if not number_match:
        return False
    
    actual_number = int(number_match.group(1))
    state = load_channel_state()
    
    # ‡∏ñ‡πâ‡∏≤‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡∏à‡∏£‡∏¥‡∏á‡πÑ‡∏°‡πà‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ö‡∏ó‡∏µ‡πà‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡πÑ‡∏ß‡πâ ‡πÉ‡∏´‡πâ‡∏ã‡∏¥‡∏á‡∏Ñ‡πå
    if actual_number != state.get("current_number", 0):
        save_channel_state(
            actual_channel_name,
            actual_number,
            actual_number  # ‡∏ã‡∏¥‡∏á‡∏Ñ‡πå pending_number ‡∏î‡πâ‡∏ß‡∏¢
        )
        print(f"üîÑ ‡∏ã‡∏¥‡∏á‡∏Ñ‡πå‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡∏ä‡πà‡∏≠‡∏á: {state.get('current_number', 0)} ‚Üí {actual_number}")
        return True
    
    return False